# run_experiment.py — Batch-run Qwen2.5-7B-Instruct 3-shot trên toàn bộ test set
# Owner: LR (Nguyễn Công Huy, SE192333)
# Platform: Kaggle Notebook, GPU Tesla T4
# Config: temperature=0.01, do_sample=False, max_new_tokens=512

import os
import time
import torch
import pandas as pd
from datetime import datetime
from transformers import AutoTokenizer, AutoModelForCausalLM
from sklearn.model_selection import train_test_split

# ── Paths (chỉnh theo môi trường) ────────────────────────────────────────────
DATA_PATH       = "/kaggle/input/datasets/huyncse192333/bugzilla-dataset/Plus14_filtered_bug_report_scores_Summary.xlsx"
OUTPUT_DIR      = "/kaggle/working/results"
OUTPUT_CSV      = f"{OUTPUT_DIR}/full_llm_output.csv"
CHECKPOINT_CSV  = f"{OUTPUT_DIR}/full_llm_output_checkpoint.csv"
LOG_FILE        = f"{OUTPUT_DIR}/full_api_log.txt"
MODEL_NAME      = "unsloth/Qwen2.5-7B-Instruct"

RANDOM_STATE    = 42
TEMPERATURE     = 0.01
MAX_NEW_TOKENS  = 512
DO_SAMPLE       = False
CHECKPOINT_EVERY = 20   # lưu checkpoint mỗi N samples

os.makedirs(OUTPUT_DIR, exist_ok=True)

# ── 3-shot prompt ─────────────────────────────────────────────────────────────
SYSTEM_PROMPT = (
    "You are an expert software tester. Given an unstructured bug description, "
    "generate a structured bug report with four sections: "
    "Steps to Reproduce (S2R), Expected Result (ER), Actual Result (AR), "
    "and Additional Information."
)
SHOT_1_INPUT = "The app crashes when I try to upload a photo larger than 5MB on the profile page."
SHOT_1_OUTPUT = (
    "#### Steps to Reproduce (S2R)\n1. Open the app and navigate to the Profile page.\n"
    "2. Tap 'Edit Profile' and select 'Upload Photo'.\n3. Choose a photo larger than 5MB.\n\n"
    "#### Expected Result (ER)\nThe photo should upload successfully.\n\n"
    "#### Actual Result (AR)\nThe app crashes immediately after selecting the photo.\n\n"
    "#### Additional Information\n- Device: Samsung Galaxy S21, Android 12\n- App version: 3.2.1"
)
SHOT_2_INPUT = "Dark mode doesn't work on the settings page. The background stays white."
SHOT_2_OUTPUT = (
    "#### Steps to Reproduce (S2R)\n1. Open the app and go to Settings.\n"
    "2. Toggle 'Dark Mode' to ON.\n3. Navigate within Settings.\n\n"
    "#### Expected Result (ER)\nAll pages should switch to dark background.\n\n"
    "#### Actual Result (AR)\nSettings page background remains white.\n\n"
    "#### Additional Information\n- OS: iOS 16.2, iPhone 13"
)
SHOT_3_INPUT = "Search results don't update when I type. I have to press Enter manually."
SHOT_3_OUTPUT = (
    "#### Steps to Reproduce (S2R)\n1. Open the app and go to Search.\n"
    "2. Tap the search box and start typing.\n3. Observe the results.\n\n"
    "#### Expected Result (ER)\nResults should update in real-time.\n\n"
    "#### Actual Result (AR)\nResults only update after pressing Enter.\n\n"
    "#### Additional Information\n- Tested on Chrome 114, Windows 10."
)

def build_prompt(user_input: str) -> str:
    return (
        f"<|im_start|>system\n{SYSTEM_PROMPT}<|im_end|>\n"
        f"<|im_start|>user\n{SHOT_1_INPUT}<|im_end|>\n"
        f"<|im_start|>assistant\n{SHOT_1_OUTPUT}<|im_end|>\n"
        f"<|im_start|>user\n{SHOT_2_INPUT}<|im_end|>\n"
        f"<|im_start|>assistant\n{SHOT_2_OUTPUT}<|im_end|>\n"
        f"<|im_start|>user\n{SHOT_3_INPUT}<|im_end|>\n"
        f"<|im_start|>assistant\n{SHOT_3_OUTPUT}<|im_end|>\n"
        f"<|im_start|>user\n{user_input}<|im_end|>\n"
        f"<|im_start|>assistant\n"
    )

# ── Load dataset ──────────────────────────────────────────────────────────────
print("Loading dataset...")
df_raw = pd.read_excel(DATA_PATH)
print(f"  Raw: {df_raw.shape[0]} rows")

_, test_df = train_test_split(df_raw, test_size=0.1, random_state=RANDOM_STATE)
test_df = test_df.reset_index(drop=True)
test_df['sample_id'] = test_df.index
print(f"  Test split (10%, seed={RANDOM_STATE}): {len(test_df)} rows")

# Resume từ checkpoint nếu có
start_idx = 0
results = []
if os.path.exists(CHECKPOINT_CSV):
    done = pd.read_csv(CHECKPOINT_CSV)
    results = done.to_dict('records')
    start_idx = len(done)
    print(f"  Resuming from checkpoint: {start_idx}/{len(test_df)} done")

# ── Load model ────────────────────────────────────────────────────────────────
print(f"\nLoading model: {MODEL_NAME}")
tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME)
model = AutoModelForCausalLM.from_pretrained(
    MODEL_NAME,
    torch_dtype=torch.float16,
    device_map="auto",
)
print(f"  GPU: {torch.cuda.get_device_name(0) if torch.cuda.is_available() else 'CPU'}")

# ── Run inference ─────────────────────────────────────────────────────────────
log_lines = [
    f"# full_api_log.txt — generated by run_experiment.py",
    f"# Start: {datetime.now().isoformat()}",
    f"# Model: {MODEL_NAME} | temp={TEMPERATURE} | do_sample={DO_SAMPLE} | max_new_tokens={MAX_NEW_TOKENS}",
    "#" + "-"*70,
]

print(f"\nRunning inference on {len(test_df) - start_idx} samples...")
for i in range(start_idx, len(test_df)):
    row = test_df.iloc[i]
    sid = int(row['sample_id'])
    input_text = str(row['NEW_llama_output'])
    ground_truth = str(row['text'])

    t0 = time.time()
    try:
        prompt = build_prompt(input_text)
        inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
        with torch.no_grad():
            outputs = model.generate(
                **inputs,
                max_new_tokens=MAX_NEW_TOKENS,
                do_sample=DO_SAMPLE,
                temperature=TEMPERATURE,
            )
        generated = tokenizer.decode(
            outputs[0][inputs["input_ids"].shape[1]:],
            skip_special_tokens=True
        )
        status = "OK"
    except Exception as e:
        generated = "INVALID"
        status = f"ERROR: {str(e)[:80]}"

    duration = time.time() - t0
    ts = datetime.now().strftime("%Y-%m-%dT%H:%M:%S")
    log_lines.append(
        f"[{ts}] sample_id={sid:03d} | model={MODEL_NAME} | "
        f"status={status} | cost=$0.00 | duration={duration:.1f}s"
    )

    results.append({
        "sample_id":          sid,
        "input_unstructured": input_text,
        "ground_truth":       ground_truth,
        "qwen_output":        generated,
    })

    # Checkpoint
    if (i + 1) % CHECKPOINT_EVERY == 0 or (i + 1) == len(test_df):
        pd.DataFrame(results).to_csv(CHECKPOINT_CSV, index=False)
        print(f"  [{i+1}/{len(test_df)}] checkpoint saved")

# ── Save final ────────────────────────────────────────────────────────────────
final_df = pd.DataFrame(results)
final_df.to_csv(OUTPUT_CSV, index=False)

ok_count = sum(1 for r in results if r['qwen_output'] != 'INVALID')
log_lines += [
    "#" + "-"*70,
    f"# End: {datetime.now().isoformat()}",
    f"# SUMMARY: N={len(results)} | OK={ok_count} | INVALID={len(results)-ok_count}",
    f"# Total cost: $0.00",
]
with open(LOG_FILE, 'w') as f:
    f.write('\n'.join(log_lines))

print(f"\n✅ Done. Saved {len(final_df)} rows → {OUTPUT_CSV}")
print(f"   OK={ok_count} | INVALID={len(results)-ok_count}")
print(f"   Log → {LOG_FILE}")
